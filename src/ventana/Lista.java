    public void adddata() {
        DefaultTableModel ta = (DefaultTableModel) tdata.getModel();
        while (ta.getRowCount() > 0) {
            ta.removeRow(0);
        }
        for (Iterator iterator = db.iterator(); iterator.hasNext();) {
            Alumno al = (Alumno) iterator.next();
            Object[] ob = new Object[2];
            ob[0] = al.getNombre();
            ob[1] = al.getEdad();
            ta.addRow(ob);
        }
    }

    public void bdato() {
        tnombre.setText("");
        tedad.setText("");
        this.edita = false;
    }

    public void udata() {
        int se = tdata.getSelectedRow();
        if (se >= 0) {
            Alumno al = new Alumno(tnombre.getText(), Integer.parseInt(tedad.getText()));
            db.set(se, al);
        }
    }

